<template>
  <div>
    <div class="demo-upload-list" v-for="item in uploadList" :key="item.value">
      <template v-if="item.status === 'finished'">
        <img :src="item.url" />
        <div class="demo-upload-list-cover">
          <Icon type="ios-eye-outline" @click.native="handleView(item.name)"></Icon>
          <Icon type="ios-trash-outline" @click.native="handleRemove(item)"></Icon>
        </div>
      </template>
      <template v-else>
        <Progress v-if="item.showProgress" :percent="item.percentage" hide-info></Progress>
      </template>
    </div>
    <Upload
      v-show="uploadList.length===0"
      ref="upload"
      :default-file-list="defaultList"
      :show-upload-list="false"
      :on-success="handleSuccess"
      :format="['jpg','jpeg','png']"
      :max-size="10240"
      :on-format-error="handleFormatError"
      :on-exceeded-size="handleMaxSize"
      :before-upload="handleBeforeUpload"
      :headers="myHeader"
      type="drag"
      name="imgFile"
      :action="baseUrl"
      style="display: inline-block;width:58px;"
    >
      <div style="width: 58px;height:58px;line-height: 58px;">
        <Icon type="ios-camera" size="20"></Icon>
      </div>
    </Upload>
    <Modal title="上传的图片" v-model="visible">
      <img :src="uploadList[0].url" v-if="visible" style="width: 100%" />
    </Modal>
  </div>
</template>
<script>
export default {
  name: 'imgUpload',
  props: ['defaultUrl'],
  data() {
    return {
      myHeader: { authorization: localStorage.getItem('hmAdminToken') },
      imgName: '',
      visible: false,
      uploadList: [],
      baseUrl: '',
      defaultList: []
    }
  },
  methods: {
    handleView(name) {
      this.imgName = name
      this.visible = true
    },
    handleRemove(file) {
      this.$emit('imgDel')
      const fileList = this.$refs.upload.fileList
      this.$refs.upload.fileList.splice(fileList.indexOf(file), 1)
    },
    handleSuccess(res, file) {
      this.$emit('imgUpload', res.data.url)
      file.url = /^http.+$/.test(res.data.url) ? res.data.url : 'http://' + res.data.url
      file.name = '头像'
      console.log(this.uploadList)
    },
    handleFormatError(file) {
      this.$Notice.warning({
        title: '上传的图片格式错误',
        desc: '上传的图片格式不对，只支持jpg,jpeg,png'
      })
    },
    handleMaxSize(file) {
      this.$Notice.warning({
        title: '超出上传限制大小',
        desc: '上传的图片最大不能超过10MB'
      })
    },
    handleBeforeUpload() {
      const check = this.uploadList.length < 1
      if (!check) {
        this.$Notice.warning({
          title: '最多只能上传一张.'
        })
      }
      return check
    },
    // 更新图片信息
    updateImgUrl(params) {
      this.$refs.upload.fileList = params
      this.uploadList = this.$refs.upload.fileList
    }
  },
  mounted() {
    this.uploadList = this.$refs.upload.fileList
  },
  created() {
    if (this.defaultUrl) {
      this.defaultList.push({
        name: 'url',
        url: this.defaultUrl
      })
    }

    this.baseUrl = this.$config.imgBaseUrl
  }
}
</script>
